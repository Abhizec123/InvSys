    private void table2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table2MouseClicked
        // TODO add your handling code here:
        DefaultTableModel model=(DefaultTableModel)table2.getModel();
        int idx=table2.getSelectedRow();
        t1.setText(model.getValueAt(idx, 0).toString());
        t2.setText(model.getValueAt(idx, 1).toString());
        t3.setText(model.getValueAt(idx, 4).toString());
        t4.setText(model.getValueAt(idx, 5).toString());
        
        String desiredItem = model.getValueAt(idx, 2).toString(); //The item you want to select
        for (int i = 0; i < combo1.getItemCount(); i++) {
            if (desiredItem.equals(combo1.getItemAt(i))) {
                combo1.setSelectedIndex(i);
                break;
            }
        }
        String dsrdItem = model.getValueAt(idx, 3).toString(); //The item you want to select
        for (int i = 0; i < combo2.getItemCount(); i++) {
            if (dsrdItem.equals(combo2.getItemAt(i))) {
                combo2.setSelectedIndex(i);
                break;
            }
        }
 
    }//GEN-LAST:event_table2MouseClicked

