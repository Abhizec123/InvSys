    private void getEntries()
    {
        try 
        {
            //To update as soon as new data is added
            javax.swing.table.DefaultTableModel dtm=(javax.swing.table.DefaultTableModel)table2.getModel();
            int rc=dtm.getRowCount();
            while(rc--!=0)
            {
                dtm.removeRow(0);
            }
            
            ResultSet rs=db.DbConnect.s.executeQuery
            ("select * from product order by ProdQty asc");
            
            BigDecimal total = new BigDecimal("0.0");
            while(rs.next())
            {            
                int b=rs.getInt("ProdQty");
                
                BigDecimal t=rs.getBigDecimal("Cprice");
                
                BigDecimal subtotal = t.multiply(BigDecimal.valueOf(b)); // Calculate the subtotal
                total = total.add(subtotal); // Add the subtotal to the total
                
                BigDecimal u=rs.getBigDecimal("Sprice");
                
                String a=rs.getString("ProdName");
                
                String c=rs.getString("ProdCat");
                String d=rs.getString("ProdBrd");
                
                Object o[]={a,b,c,d,t,u};
                dtm.addRow(o);
            }
            sp.setText(total.toString());
        }
        catch (Exception ex) 
        {
            JOptionPane.showMessageDialog(this,ex);
        }
    }

