    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try
        {
            String c=t4.getText();
            if(!c.equals(""))
            {
                //Inserting using createStatement
                String sql="insert into brand values ('"+c+"')";
                
                db.DbConnect.s.executeUpdate(sql);

                JOptionPane.showMessageDialog(this, "Brand Added Successfully!");
                getEntries();//updating the table

                //db.DbConnect.con.close();
            }
            else
                JOptionPane.showMessageDialog(this, "Please Enter Brand Name");
            
        }
        catch(SQLIntegrityConstraintViolationException ex)
        {
            JOptionPane.showMessageDialog(this,"Brand Already Exist");
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this,ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int rs=table1.getSelectedRow();
        if(rs!=-1)
        {
            int r=JOptionPane.showConfirmDialog
        (null, "Do you really want to delete?", "Confirmation", JOptionPane.YES_NO_OPTION);
        
            if(r==JOptionPane.YES_OPTION)
            {
                String cat=(String)table1.getValueAt(rs, 1);

                try
                {
                    db.DbConnect.s.executeUpdate("delete from brand where brdName='"+cat+"' ");
                    JOptionPane.showMessageDialog(this,"Category Deleted Successfully!");
                    getEntries();//refreshing table
                }
                catch(Exception ex)
                {
                    JOptionPane.showMessageDialog(this,ex);
                } 
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Please Select a Row");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

